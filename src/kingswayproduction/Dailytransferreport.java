    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        try{
            
            
             try {
            Date d = jDateChooser1.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            dd = sdf.format(d);
           
        } catch (Exception e) {
            e.printStackTrace();
        }
            
            
            
        String sql ="Select * from Finalgood where Section ='"+jComboBox1.getSelectedItem()+"' and Date = '"+dd+"'";  //
        pst=con.prepareStatement(sql);
        rs=pst.executeQuery();
        int productqty = 0;
        String name = "";
            while (rs.next()) {
                
                productqty =rs.getInt("Qty");
                name = rs.getString("Name");
               
            }
          
        
           
           
           
            DefaultTableModel de = (DefaultTableModel) jTable1.getModel();
            JRTableModelDataSource datasource = new JRTableModelDataSource(de);
            String reportSource = "C://Kinsway_Production_Report//transferreport.jrxml";

            JasperReport jr = JasperCompileManager.compileReport(reportSource);

            Map<String, Object> params = new HashMap<String, Object>();
            params.put("Section", jComboBox1.getSelectedItem().toString()+"");
            params.put("Date", dd+"");
            params.put("Qty", productqty+"");
            params.put("Name", name+"");
           
           
           
            JasperPrint jp = JasperFillManager.fillReport(jr, params, datasource);


            JasperViewer.viewReport(jp, false);
      
            
        
        
        }catch(Exception e){
            
          JOptionPane.showMessageDialog(null,e);
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

