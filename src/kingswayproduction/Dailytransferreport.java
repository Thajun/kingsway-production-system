       private void UpdateSearchTable(){
        
          try {
            Date d = jDateChooser1.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            dd = sdf.format(d);
           
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
        
       try{
        String sql ="Select * from Transferedmaterials where Section = '"+jComboBox1.getSelectedItem()+"' and Date ='"+dd+"'";
        pst=con.prepareStatement(sql);
        rs=pst.executeQuery();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            while (rs.next()) {
               
                Vector v = new Vector();
               v.add(rs.getString("Date"));
                v.add(rs.getString("Section"));
                v.add(rs.getString("Type"));
                v.add(rs.getString("Name"));                
                v.add(rs.getString("Qty")+" "+rs.getString("Unit"));                             
                v.add(rs.getDouble("Cost"));
                dtm.addRow(v);
            }
        
            
        }catch(Exception e){
            
           JOptionPane.showMessageDialog(null,e);
        }
        
    }  
      
    
